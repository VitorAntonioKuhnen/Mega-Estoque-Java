    private void btLogarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btLogarActionPerformed
        String login = varLogin.getText().trim();
        String senha = new String(this.varSenha.getPassword());

        try {

            if(negocioEstoque.validaLogin(usuario, login, senha).equals("Correto")){
                System.out.println("Logado");
                new Principal().setVisible(true);
                dispose();
            }
        } catch (Exception ex) {
            System.err.println("Usuario informado n√£o encontrado " + ex.getMessage());
            lbErro.setVisible(true);
        }
    }//GEN-LAST:event_btLogarActionPerformed

